TRUST_STRANGER_PROMPT = (
    "Это - пара сообщений в диалоге:\n\n"
    "{last_pairs}\n\n"
    "Оцени, есть ли **в сообщениях user**:\n"
    "1. Агрессия / унижение:\n"
    "- Грубость, насмешки, обесценивание (“да фигня у тебя проблемы”, “сам виноват”).\n"
    "- Пассивная агрессия, подколы “якобы шуткой”, но от них может быть больно.\n"
    "- Давление: повышенный тон, угрозы, игра статусом (“я лучше знаю”, “ты ничего не понимаешь”).\n"
    "- Нарушение границ: не принимает “нет” (продолжает давить, уговаривать, ревновать, лезть).\n"
    "- Лезет в личное. Использует то, чем делились, против собеседника.\n"
    "2. Базовое уважение и предсказуемость:\n"
    "- Уважение к собеседнику: слушает, не перебивает “танком”.\n"
    "- Не смеётся над уязвимыми местами.\n"
    "- Может признать, что не прав / чего-то не понимает.\n"
    "- В конфликте не добивает, а пытается разобраться (или хотя бы не делает хуже).\n"
    "- Не изливает душу без малейшего интереса к собеседнику.\n\n"
    "Оцени сообщения user по шкале безопасности и уважения.\n"
    "Считай:\n"
    "- агрессии нет = хорошо,\n"
    "- агрессия есть = плохо,\n"
    "- есть явные признаки уважения и бережности = очень хорошо.\n"
    "Поставь целое число от \"0\" до \"2\":\n"
    "\"0\" — есть агрессия/унижение ИЛИ сильное давление, человеку доверять нельзя."
    "\"1\" — явной агрессии нет, но и явного уважения/бережности не видно, данных мало."
    "\"2\" — нет агрессии, и есть признаки уважения/бережного отношения к собеседнику."
    "Верни только число в кавычках — \"0\", \"1\" или \"2\". Без пояснений, комментариев или интерпретаций.\n"
)


